fun main() {
    //Arithmetic operators
    var num1 = 27
    var num2 = 21
    println(num1 + num2)
    println(num1 - num2)
    println(num1 * num2)
    println(num1 / num2)
    println(num1 % num2)

    //Comparison operators
    println(num1 > num2)
    println(num1 < num2)
    println(num1 <= num2)
    println(num1 >= num2)
    println(num1 == num2) //Equal to
    println(num1 != num2) // Not equal to

    //Logic operators
    println(num1 < num1 && num1 != num2)
    println(num1 < num1 || num1 != num2)
    println(!(num1 < num1 || num1 != num2))

    //Assignment operators
    var x = 23
    x +7
    println(x)
}
